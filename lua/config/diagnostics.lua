-- ============================================================================
-- lua/config/diagnostics.lua
-- Глобальная настройка отображения диагностик от LSP, линтеров и
-- других источников. Это часть встроенного API Neovim (vim.diagnostic),
-- а не плагин — поэтому живёт в config/, не в plugins/.
--
-- Применяется ко всем будущим LSP-серверам (gopls, yamlls и т.п.):
-- настраиваем один раз, работает везде.
-- ============================================================================

-- ──────────────────────────────────────────────────────────────────────
-- Иконки уровней (signs в gutter — узкая колонка слева от номера строки).
-- Nerd Font у нас есть, поэтому глифы из шрифта рендерятся корректно.
--
-- Глифы записаны через "\u{XXXX}" (Lua escape для Unicode code-point),
-- а не как сами символы в исходнике. Так файл остаётся чистым ASCII и
-- никакая инфраструктура (мессенджеры, буфер обмена, IME) не может
-- "съесть" символ из приватной зоны Nerd Font.
--   "\u{ea87}"  = "" (Nerd Font: cod-error)
--   "\u{ea6c}"  = "" (Nerd Font: cod-warning)
--   "\u{ea74}"  = "" (Nerd Font: cod-info)
--   "\u{f0166}" = "󰌶" (Nerd Font: md-lightbulb)
--
-- Уровни (от высшего к низшему):
--   ERROR — что-то сломано (не компилится, неверный тип).
--   WARN  — можно жить, но плохо (unused var, shadowing).
--   INFO  — информационное сообщение.
--   HINT  — мягкая подсказка (например, deprecation).
-- ──────────────────────────────────────────────────────────────────────
local signs = {
  [vim.diagnostic.severity.ERROR] = "\u{ea87}",
  [vim.diagnostic.severity.WARN]  = "\u{ea6c}",
  [vim.diagnostic.severity.INFO]  = "\u{ea74}",
  [vim.diagnostic.severity.HINT]  = "\u{f0166}",
}

-- ──────────────────────────────────────────────────────────────────────
-- Основная конфигурация. vim.diagnostic.config принимает таблицу с
-- ключами по способам отображения. Любой из них можно отключить,
-- передав false вместо таблицы настроек.
-- ──────────────────────────────────────────────────────────────────────
vim.diagnostic.config({
  -- Иконка в gutter слева от строки. text = таблица иконок по уровню.
  signs = {
    text = signs,
    -- numhl: подкрашивать номер строки в цвет уровня. Хорошо заметно
    -- даже без virtual_text — видишь красный номер, знаешь что там
    -- ошибка.
    numhl = {
      [vim.diagnostic.severity.ERROR] = "DiagnosticSignError",
      [vim.diagnostic.severity.WARN]  = "DiagnosticSignWarn",
      [vim.diagnostic.severity.INFO]  = "DiagnosticSignInfo",
      [vim.diagnostic.severity.HINT]  = "DiagnosticSignHint",
    },
  },

  -- Подчёркивание под проблемным куском кода — та самая "волнистая
  -- красная линия" из VS Code. Стиль (волна/прямая/точки) зависит от
  -- терминала, kitty умеет undercurl.
  underline = true,

  -- Виртуальный текст справа от строки кода. Не настоящие символы в
  -- буфере, а оверлей. prefix = "\u{25CF}" — кружок-маркер перед сообщением.
  -- spacing = 2 — два пробела отступа от кода.
  -- source = "if_many" — добавлять имя источника (например "gopls")
  -- только если на одной строке несколько источников; иначе шум.
  virtual_text = {
    prefix  = "\u{25CF}", -- ● (BLACK CIRCLE, обычный Unicode, не Nerd Font)
    spacing = 2,
    source  = "if_many",
  },

  -- Floating-окно: появляется при <leader>ld (биндинг ниже) или при
  -- наведении (если в Блоке 14 включим CursorHold-автокоманду).
  -- border = "rounded" — единый стиль рамок со всем UI.
  -- source = "if_many" — то же поведение, что и у virtual_text.
  -- header = "" — не пишем заголовок "Diagnostics:", сообщение и так
  -- говорит само за себя.
  -- prefix = "" — без префикса перед сообщением.
  float = {
    border = "rounded",
    source = "if_many",
    header = "",
    prefix = "",
  },

  -- НЕ обновлять диагностики, пока ты печатаешь в Insert-режиме.
  -- Обновятся, как только выйдешь в Normal. Иначе при каждом нажатии
  -- клавиши перерисовываются "волны" — мешает читать и тормозит.
  update_in_insert = false,

  -- Если на одной строке и ошибка, и warning — показать ошибку
  -- первой (она важнее). Касается signs, virtual_text, float.
  severity_sort = true,
})

-- ──────────────────────────────────────────────────────────────────────
-- Глобальные биндинги для навигации по диагностикам.
-- В Neovim 0.10+ ]d и [d работают "из коробки" через стандартные
-- mappings, но мы зафиксируем их явно — на случай, если в будущем
-- plugin или config переопределит.
--
-- ВАЖНО: LSP-биндинги (K, gd, gr, <leader>la и т.п.) НЕ здесь —
-- они вешаются через LspAttach autocmd только когда LSP реально
-- приатачен к буферу. Диагностические биндинги — глобальные,
-- работают независимо от LSP (могут показывать диагностики от
-- линтеров, treesitter и т.д.).
-- ──────────────────────────────────────────────────────────────────────
local map = vim.keymap.set

-- ]d / [d — следующая / предыдущая диагностика в текущем буфере.
-- В VS Code это F8 / Shift+F8.
map("n", "]d", function() vim.diagnostic.jump({ count =  1 }) end, { desc = "Next diagnostic" })
map("n", "[d", function() vim.diagnostic.jump({ count = -1 }) end, { desc = "Prev diagnostic" })

-- <leader>ld — открыть floating-окно с полным текстом диагностики
-- под курсором. Спасает, когда virtual_text обрезан или сообщение
-- длинное и не помещается в строку.
map("n", "<leader>ld", function() vim.diagnostic.open_float() end, { desc = "Line diagnostic (float)" })