-- ============================================================================
-- lua/config/keymaps.lua
-- Глобальные пользовательские биндинги, не зависящие от плагинов.
-- Биндинги, привязанные к плагинам, живут внутри lua/plugins/<имя>.lua.
--
-- Шпаргалка по аргументам vim.keymap.set:
--   modes  — режим(ы): "n" Normal, "i" Insert, "v" Visual, "t" Terminal,
--            "c" Command, "x" Visual-block, "" — все обычные режимы.
--   lhs    — что нажать (например "<leader>R" или "<C-s>").
--   rhs    — что выполнить: либо строка-команда (":w<CR>"), либо
--            Lua-функция (function() ... end).
--   opts   — таблица опций. desc обязателен — он попадёт в which-key.
-- ============================================================================

local map = vim.keymap.set

-- ──────────────────────────────────────────────────────────────────────
-- Перезагрузка конфига без перезапуска Neovim
-- ──────────────────────────────────────────────────────────────────────
-- <leader>R — повторно подгрузить options/keymaps/autocmds. 
--
-- ВАЖНО: это работает только для опций, биндингов и автокоманд. Правки
-- в init.lua или lazy.lua, а также установка/удаление плагинов
-- требуют полного перезапуска Neovim.
map("n", "<leader>R", function()
  for name, _ in pairs(package.loaded) do
    if name:match("^config%.") then
      package.loaded[name] = nil
    end
  end
  require("config.options")
  require("config.keymaps")
  require("config.autocmds")
  vim.notify("Конфиг перезагружен", vim.log.levels.INFO)
end, { desc = "Reload config" })

-- ──────────────────────────────────────────────────────────────────────
-- Сохранение и выход
-- ──────────────────────────────────────────────────────────────────────
-- <C-s> — сохранить текущий буфер. Привычка из VS Code.
-- Работает в Normal и Insert режимах. В Insert после сохранения
-- остаёмся в Insert (через <Esc>...gi возвращаемся к курсору).
map({ "n", "i", "v" }, "<C-s>", "<Esc>:w<CR>", { desc = "Save file" })

-- <leader>q — закрыть текущее окно (без сохранения предупредит).
map("n", "<leader>q", "<cmd>q<CR>", { desc = "Quit window" })

-- <leader>Q — закрыть всё (с сохранением). Аналог :wqa.
map("n", "<leader>Q", "<cmd>wqa<CR>", { desc = "Save all and quit" })

-- ──────────────────────────────────────────────────────────────────────
-- Навигация между окнами одной клавишей
-- ──────────────────────────────────────────────────────────────────────
-- Стандартный <C-w> h/j/k/l — это две клавиатурные комбинации.
-- Сокращаем до одной: <C-h/j/k/l>. h/j/k/l — святой квадрат Vim
-- (лево/низ/верх/право), здесь они переключают окна в этих направлениях.
map("n", "<C-h>", "<C-w>h", { desc = "Window: left" })
map("n", "<C-j>", "<C-w>j", { desc = "Window: down" })
map("n", "<C-k>", "<C-w>k", { desc = "Window: up" })
map("n", "<C-l>", "<C-w>l", { desc = "Window: right" })

-- Изменение размеров окон стрелками с Ctrl. Удобно, когда сплит
-- получился неравномерным. Шаг — 2 строки/колонки.
map("n", "<C-Up>",    "<cmd>resize +2<CR>",          { desc = "Window: increase height" })
map("n", "<C-Down>",  "<cmd>resize -2<CR>",          { desc = "Window: decrease height" })
map("n", "<C-Left>",  "<cmd>vertical resize -2<CR>", { desc = "Window: decrease width" })
map("n", "<C-Right>", "<cmd>vertical resize +2<CR>", { desc = "Window: increase width" })

-- ──────────────────────────────────────────────────────────────────────
-- Поиск и подсветка
-- ──────────────────────────────────────────────────────────────────────
-- Когда ищешь через /pattern, после Enter все совпадения остаются
-- подсвеченными. <Esc> в Normal-режиме снимает подсветку.
-- Поведение из VS Code: ищешь, читаешь, тыкаешь Esc — подсветка ушла.
map("n", "<Esc>", "<cmd>nohlsearch<CR>", { desc = "Clear search highlight" })

-- Центрировать экран после прыжка к следующему/предыдущему совпадению.
-- В Vim n/N прыгают между совпадениями, но курсор может оказаться
-- у самого края экрана. zz после — центрирует.
map("n", "n", "nzzzv", { desc = "Next search match (centered)" })
map("n", "N", "Nzzzv", { desc = "Prev search match (centered)" })

-- Центрировать экран после <C-d>/<C-u> (прокрутка на полстраницы).
map("n", "<C-d>", "<C-d>zz", { desc = "Half page down (centered)" })
map("n", "<C-u>", "<C-u>zz", { desc = "Half page up (centered)" })

-- ──────────────────────────────────────────────────────────────────────
-- Редактирование
-- ──────────────────────────────────────────────────────────────────────
-- Y — копировать от курсора до конца строки (в Vim по умолчанию это
-- то же, что yy = вся строка, что нелогично). Делаем так, как
-- ожидается: D копирует до конца строки, C изменяет до конца строки,
-- Y теперь тоже работает до конца строки.
map("n", "Y", "y$", { desc = "Yank to end of line" })

-- При вставке поверх выделения (Visual mode + p) Vim перезатирает
-- регистр скопированным. Это раздражает: скопировал X, заменил Y на X,
-- теперь не можешь снова вставить X — в регистре оказался Y.
-- "_dP — удаляем в "чёрную дыру" (регистр _) и потом вставляем.
map("x", "p", '"_dP', { desc = "Paste without yanking replaced text" })

-- В Visual-режиме после > или < (увеличить/уменьшить отступ) выделение
-- сбрасывается. Делаем так, чтобы оставалось — можно подвигать туда-сюда.
map("v", "<", "<gv", { desc = "Indent left, keep selection" })
map("v", ">", ">gv", { desc = "Indent right, keep selection" })

-- Двигать выделенные строки в Visual mode через J/K (вниз/вверх).
-- = в конце автоматически переотступает строки после перемещения.
map("v", "J", ":m '>+1<CR>gv=gv", { desc = "Move selection down" })
map("v", "K", ":m '<-2<CR>gv=gv", { desc = "Move selection up" })

-- ──────────────────────────────────────────────────────────────────────
-- Буферы (открытые файлы)
-- ──────────────────────────────────────────────────────────────────────
-- В Neovim "буфер" = открытый в памяти файл. Это не то же, что окно:
-- один буфер может быть виден в нескольких окнах, и одно окно может
-- по очереди показывать разные буферы. Аналог — "вкладки" в VS Code,
-- но без визуального ряда наверху (его добавит bufferline.nvim в Блоке 4).
--
-- <S-h>/<S-l> = Shift+h / Shift+l — переключение между буферами.
map("n", "<S-h>", "<cmd>bprevious<CR>", { desc = "Previous buffer" })
map("n", "<S-l>", "<cmd>bnext<CR>",     { desc = "Next buffer" })

-- <leader>bd — закрыть текущий буфер, не закрывая окно.
map("n", "<leader>bd", "<cmd>bdelete<CR>", { desc = "Close buffer" })

-- ──────────────────────────────────────────────────────────────────────
-- Терминал-режим
-- ──────────────────────────────────────────────────────────────────────
-- Когда внутри Neovim открыт терминал (это будет после Блока 8),
-- стандартный <Esc> идёт в shell, а не в Neovim. Делаем двойной <Esc>
-- кнопкой выхода из терминал-режима в Normal.
map("t", "<Esc><Esc>", "<C-\\><C-n>", { desc = "Exit terminal mode" })

