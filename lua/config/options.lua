-- ============================================================================
-- lua/config/options.lua
-- Глобальные настройки самого Neovim
-- ============================================================================

local opt = vim.opt

-- ──────────────────────────────────────────────────────────────────────
-- Внешний вид и колонки
-- ──────────────────────────────────────────────────────────────────────

-- Абсолютный номер текущей строки + относительные номера остальных.
-- Очень удобно для прыжков: "10j" = "вниз на 10 строк", и видно сразу,
-- сколько именно нажать. В VS Code такого нет.
opt.number = true
opt.relativenumber = true

-- Подсветить строку, на которой стоит курсор (как в VS Code).
opt.cursorline = true

-- Колонка слева (sign column) для значков LSP/git/диагностик.
-- "yes" = всегда видна, чтобы текст не дёргался при появлении значков.
opt.signcolumn = "yes"

-- Не переносить длинные строки визуально на следующую строку.
-- Длинная строка уезжает за правый край, прокручиваем горизонтально.
-- Для кода это правильнее: видно реальную форму строки.
opt.wrap = false

-- Минимум строк сверху и снизу от курсора при прокрутке.
-- 8 = курсор не «прилипает» к верху/низу экрана, контекст всегда виден.
opt.scrolloff = 8
opt.sidescrolloff = 8

-- ──────────────────────────────────────────────────────────────────────
-- Отступы и табуляция
-- ──────────────────────────────────────────────────────────────────────

-- ВАЖНО для Go: gofmt всегда использует ТАБЫ, а не пробелы.
-- Поэтому expandtab = false. Для других языков (yaml, lua) этим
-- займётся плагин по filetype.
opt.expandtab = false

-- Ширина одного отступа: 4 визуальных пробела. Это просто
-- ширина отображения таба; в файле всё равно остаётся реальный \t.
opt.tabstop = 4
opt.softtabstop = 4
opt.shiftwidth = 4

-- Автоматический отступ новой строки на уровень предыдущей.
opt.autoindent = true
opt.smartindent = true

-- ──────────────────────────────────────────────────────────────────────
-- Поиск
-- ──────────────────────────────────────────────────────────────────────

-- Регистронезависимый поиск ПО УМОЛЧАНИЮ.
opt.ignorecase = true

-- Но если в запросе есть заглавная буква — поиск становится
-- регистрозависимым автоматически. "foo" ищет всё, "Foo" ищет только Foo.
opt.smartcase = true

-- Подсвечивать ВСЕ совпадения поиска, не только текущее.
opt.hlsearch = true

-- Подсвечивать совпадение по мере набора, не дожидаясь Enter.
opt.incsearch = true

-- ──────────────────────────────────────────────────────────────────────
-- Файлы, undo, бэкапы
-- ──────────────────────────────────────────────────────────────────────

-- Persistent undo: история отмены сохраняется между запусками Neovim.
-- Закрыл файл вчера — открыл сегодня — всё ещё можешь жать `u`.
opt.undofile = true
opt.undodir = vim.fn.stdpath("state") .. "/undo"

-- Не создавать .swp/.bak файлы рядом с редактируемыми.
-- В git-репозитории они только мешают.
opt.swapfile = false
opt.backup = false
opt.writebackup = false

-- ──────────────────────────────────────────────────────────────────────
-- Поведение интерфейса и редактирования
-- ──────────────────────────────────────────────────────────────────────

-- Системный буфер обмена. "unnamedplus" = всё, что копируешь в Neovim
-- (через y), попадает в Ctrl+V вне Neovim, и наоборот.
-- Это тот самый "копировать как в нормальном редакторе".
opt.clipboard = "unnamedplus"

-- Включить мышь во всех режимах. Можно кликнуть, выделить, проскроллить.
opt.mouse = "a"

-- Поддержка 24-битных цветов в терминале — нужно для нормальных
-- цветовых схем. kitty это поддерживает.
opt.termguicolors = true

-- Тёмный фон: подсказывает плагинам выбирать тёмные варианты.
opt.background = "dark"

-- Время до срабатывания CursorHold (используется LSP/плагинами для
-- показа hover-подсказок при наведении). Дефолт 4000мс — слишком долго.
opt.updatetime = 250

-- Время ожидания продолжения многоклавишной команды.
-- 300мс достаточно, чтобы успеть нажать вторую клавишу <leader>-команды.
opt.timeoutlen = 300

-- Не показывать в командной строке "-- INSERT --" / "-- VISUAL --".
-- Это будет показывать lualine (наша статусная строка).
opt.showmode = false

-- Открывать новые сплиты СПРАВА и СНИЗУ (а не слева/сверху).
-- Это интуитивнее: новое окно появляется в свободном месте, а не
-- расталкивает существующие.
opt.splitright = true
opt.splitbelow = true

-- Разрешить переключение между буферами без сохранения. Иначе Neovim
-- ругается "no write since last change". Мы взрослые, сами разберёмся.
opt.hidden = true

-- Завершение команд в командной строке (после `:`) с подсказками.
opt.wildmenu = true
opt.wildmode = "longest:full,full"

-- Не подавать звуковой/визуальный сигнал при ошибках.
opt.errorbells = false
opt.visualbell = false

-- Опции для сообщений: "I" = не показывать вступительный экран Neovim
-- (тот самый с "Бедным детям в Уганде"). Когда подключим dashboard
opt.shortmess:append("I")

-- Список символов для отображения "невидимых" знаков (табы, пробелы
-- в конце строк). Удобно для отладки YAML и Makefile.
opt.list = true
opt.listchars = {
  tab = "▸ ",       -- табы рисуем как ▸ + пробел
  trail = "·",      -- пробелы в конце строки — точкой
  nbsp = "␣",       -- неразрывный пробел (часто незаметный баг)
}
-- Скрыть маркер ~ (EndOfBuffer) на пустых строках после конца буфера.
-- Заменяем символ на пробел — стандартный приём современных конфигов.
opt.fillchars = {
  eob = " ",
}

-- Сворачивание (folding) пока выключаем — настроим через nvim-ufo
opt.foldenable = false

-- Отключение неиспользуемых провайдеров
vim.g.loaded_python3_provider = 0
vim.g.loaded_ruby_provider = 0
vim.g.loaded_perl_provider = 0
vim.g.loaded_node_provider = 0