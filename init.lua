-- ============================================================================
-- ~/.config/nvim/init.lua
-- Точка входа. Именно этот файл Neovim ищет при запуске.
-- Порядок важен: leader-клавишу нужно задать ДО загрузки плагинов.
-- ============================================================================

-- Leader-клавиша. Должна быть выставлена до подключения плагинов,
-- иначе плагины зарегистрируют свои <leader>... биндинги на старом
-- (дефолтном `\`) лидере, и наши шорткаты не будут работать.
vim.g.mapleader = " "
vim.g.maplocalleader = " "

-- Глобальные опции редактора (vim.opt.*). Подключаются ДО плагинов,
-- чтобы плагины при инициализации видели уже выставленный termguicolors,
-- background и т.п.
require("config.options")

require("config.filetypes")

-- Глобальные пользовательские биндинги (не плагинные).
require("config.keymaps")

-- Настройка отображения диагностик (vim.diagnostic.config) +
-- глобальные биндинги ]d / [d / <leader>ld. Подключается ДО плагинов,
-- чтобы первый же LSP-сервер при старте увидел уже сконфигурированный
-- vim.diagnostic.
require("config.diagnostics")

-- Автокоманды — реакции на события Neovim (yank highlight, восстановление
-- позиции курсора, отступы по filetype и т.п.).
require("config.autocmds")

-- Bootstrap менеджера плагинов и его настройка.
require("config.lazy")